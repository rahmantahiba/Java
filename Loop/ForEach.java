package Loop;

public class ForEach {

    //Used to iterate through elements of arrays and collections

    public static void main(String[] args) {
        
        //Declaring an array. The syntax is []
        int arr[] = {12,23,44,56,78}; 
        for(int i: arr) {
            System.out.println(i); 
        }
    }
    
}
